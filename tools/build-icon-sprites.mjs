#!/usr/bin/env node
// Builds curated SVG symbol sprites from open-source icon packages.
//
// Reads assets/icons/sprite-manifest.json, fetches each donor package with
// `npm pack` into .icon-donors/ (gitignored), and writes one sprite file per
// family under assets/icons/. Only the icons named in the manifest are
// vendored, per docs/asset-policy.md.
//
// Usage:
//   node tools/build-icon-sprites.mjs            # build all families
//   node tools/build-icon-sprites.mjs macos      # build one family
//   node tools/build-icon-sprites.mjs --list     # show families and icon counts

import { execSync } from "node:child_process";
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const manifestPath = path.join(root, "assets/icons/sprite-manifest.json");
const donorCache = path.join(root, ".icon-donors");

const manifest = JSON.parse(fs.readFileSync(manifestPath, "utf8"));
const args = process.argv.slice(2);

if (args.includes("--list")) {
  for (const fam of manifest.families) {
    console.log(`${fam.id}: ${Object.keys(fam.icons).length} icons <- ${fam.package}@${fam.version} (${fam.license})`);
  }
  process.exit(0);
}

const only = args.filter((a) => !a.startsWith("--"));

function ensurePackage(fam) {
  const pkgDir = path.join(donorCache, `${fam.package.replace(/[@/]/g, "_")}@${fam.version}`);
  if (fs.existsSync(path.join(pkgDir, "package"))) return pkgDir;
  fs.mkdirSync(pkgDir, { recursive: true });
  console.log(`fetching ${fam.package}@${fam.version} ...`);
  const tarball = execSync(`npm pack ${fam.package}@${fam.version} --silent`, {
    cwd: pkgDir,
    encoding: "utf8",
  }).trim().split("\n").pop();
  execSync(`tar -xzf ${JSON.stringify(tarball)}`, { cwd: pkgDir });
  return pkgDir;
}

// Attributes hoisted from the donor <svg> root onto the <symbol> so stroke
// styling (Lucide) and fill styling survive extraction.
const KEEP_ATTRS = [
  "viewBox",
  "fill",
  "stroke",
  "stroke-width",
  "stroke-linecap",
  "stroke-linejoin",
];

function svgToSymbol(svgText, symbolId) {
  const openTag = svgText.match(/<svg\b[^>]*>/);
  if (!openTag) throw new Error(`no <svg> root for ${symbolId}`);
  const attrs = {};
  for (const name of KEEP_ATTRS) {
    const m = openTag[0].match(new RegExp(`\\b${name}="([^"]*)"`));
    if (m) attrs[name] = m[1];
  }
  let inner = svgText
    .slice(svgText.indexOf(openTag[0]) + openTag[0].length, svgText.lastIndexOf("</svg>"))
    .trim();
  // Drop donor license comments inside the body; provenance lives in the
  // sprite header and README instead.
  inner = inner.replace(/<!--[\s\S]*?-->/g, "").trim();
  const attrText = Object.entries(attrs)
    .map(([k, v]) => ` ${k}="${v}"`)
    .join("");
  return `  <symbol id="${symbolId}"${attrText}>\n    ${inner.replace(/\n\s*/g, "\n    ")}\n  </symbol>`;
}

let failures = 0;

for (const fam of manifest.families) {
  if (only.length && !only.includes(fam.id)) continue;
  const pkgDir = ensurePackage(fam);
  const svgRoot = path.join(pkgDir, "package", fam.svgDir);
  const symbols = [];
  for (const [symbolName, file] of Object.entries(fam.icons)) {
    const svgPath = path.join(svgRoot, file);
    if (!fs.existsSync(svgPath)) {
      console.error(`  MISSING ${fam.id}: ${file}`);
      failures += 1;
      continue;
    }
    symbols.push(svgToSymbol(fs.readFileSync(svgPath, "utf8"), `${fam.prefix}-${symbolName}`));
  }
  const header = [
    `<!-- Generated by tools/build-icon-sprites.mjs. Do not edit by hand. -->`,
    `<!-- Source: ${fam.package}@${fam.version} (${fam.license}) - ${fam.homepage} -->`,
    `<!-- Curated subset for UI Backlot ${fam.id} surfaces; see docs/asset-policy.md -->`,
  ].join("\n");
  const sprite = `${header}\n<svg xmlns="http://www.w3.org/2000/svg" style="display:none" aria-hidden="true">\n${symbols.join("\n")}\n</svg>\n`;
  const outPath = path.join(root, fam.output);
  fs.mkdirSync(path.dirname(outPath), { recursive: true });
  fs.writeFileSync(outPath, sprite);
  console.log(`wrote ${fam.output} (${symbols.length} symbols)`);
}

process.exit(failures ? 1 : 0);
