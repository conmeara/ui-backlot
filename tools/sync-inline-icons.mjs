#!/usr/bin/env node
// Syncs the inline <symbol> definitions duplicated across compositions/ and
// surfaces/ HTML files against the canonical vendored icon sprites, so the
// copy-pasted icon markup stops drifting.
//
// Every composition/surface HTML file inlines its own
// `<svg style="display:none"><symbol id="...">...</symbol>...</svg>` block
// (Lucide, Framework7, Fluent, Simple Icons) so the file stays fully
// self-contained for file:// captures and HyperFrames rendering. This script
// does NOT change that structure — it only rewrites each symbol's inner
// markup/attributes to match the canonical source when the ids match.
//
// Canonical sources:
//   1. The sprite files listed as "output" in assets/icons/sprite-manifest.json
//      (generated by tools/build-icon-sprites.mjs from vendored packages).
//   2. assets/icons/source-authentic/backlot-custom-symbols.svg — custom,
//      non-vendored symbols (currently just #si-claude, sourced from
//      compositions/claude-desktop.html) that are duplicated across files
//      and need one source of truth. This script can regenerate that file
//      with --write-custom-symbols; normally it's checked into the repo.
//
// Usage:
//   node tools/sync-inline-icons.mjs --check   [files...]   # exit 1 if drifted
//   node tools/sync-inline-icons.mjs --write   [files...]   # rewrite in place
//   node tools/sync-inline-icons.mjs --write-custom-symbols  # regenerate
//       assets/icons/source-authentic/backlot-custom-symbols.svg from
//       compositions/claude-desktop.html and report conflicts across files.
//
// If no file args are given, defaults to every *.html in compositions/ and
// surfaces/.

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const manifestPath = path.join(root, "assets/icons/sprite-manifest.json");
const customSymbolsPath = path.join(
  root,
  "assets/icons/source-authentic/backlot-custom-symbols.svg",
);

const args = process.argv.slice(2);
const mode = args.includes("--write")
  ? "write"
  : args.includes("--check")
    ? "check"
    : args.includes("--write-custom-symbols")
      ? "write-custom-symbols"
      : null;

if (!mode) {
  console.error(
    "usage: node tools/sync-inline-icons.mjs --check|--write|--write-custom-symbols [files...]",
  );
  process.exit(2);
}

const fileArgs = args.filter((a) => !a.startsWith("--"));

// ---------------------------------------------------------------------------
// Parsing helpers
// ---------------------------------------------------------------------------

// Matches a full <symbol id="...">...</symbol> block (non-self-closing,
// which is the only form used across this repo).
const SYMBOL_RE = /<symbol\b([^>]*)>([\s\S]*?)<\/symbol>/g;

// A comment immediately preceding a <symbol> tag containing any of these
// signals means the symbol is a deliberate, hand-authored override of the
// vendored/canonical glyph (e.g. compositions/mac-menu-bar.html's
// f7-control-center, which intentionally replaces the Framework7 chevron).
// Such symbols must never be silently overwritten by the sync.
const OVERRIDE_SIGNAL_RE =
  /hand-authored|hand authored|\boverride\b|deliberate|intentional|reads wrong|do not sync|do-not-sync|do not replace|do-not-replace/i;

function extractIdAttr(attrText) {
  const m = attrText.match(/\bid="([^"]*)"/);
  return m ? m[1] : null;
}

/**
 * Parse all <symbol> blocks out of an SVG/HTML text blob.
 * Returns a Map<id, { full, attrs, inner, protected, protectReason }> where
 * `full` is the entire `<symbol ...>...</symbol>` string, in first-seen
 * order. `protected` is true when an immediately-preceding HTML comment
 * marks the symbol as a deliberate hand-authored override that must not be
 * synced from the canonical map.
 */
function parseSymbols(text) {
  const map = new Map();
  let m;
  SYMBOL_RE.lastIndex = 0;
  while ((m = SYMBOL_RE.exec(text))) {
    const [full, attrs, inner] = m;
    const id = extractIdAttr(attrs);
    if (!id) continue;
    if (map.has(id)) continue;

    // Look at the text immediately before this <symbol> for a preceding
    // HTML comment (allowing only whitespace between comment and tag).
    const before = text.slice(0, m.index);
    const commentMatch = before.match(/<!--([\s\S]*?)-->\s*$/);
    const isProtected = Boolean(commentMatch && OVERRIDE_SIGNAL_RE.test(commentMatch[1]));

    map.set(id, {
      full,
      attrs,
      inner,
      protected: isProtected,
      protectReason: isProtected ? commentMatch[1].trim().replace(/\s+/g, " ") : null,
    });
  }
  return map;
}

// ---------------------------------------------------------------------------
// Build the canonical symbol map
// ---------------------------------------------------------------------------

function loadManifestSpriteFiles() {
  const manifest = JSON.parse(fs.readFileSync(manifestPath, "utf8"));
  return manifest.families.map((fam) => path.join(root, fam.output));
}

function buildCanonicalMap() {
  const canonical = new Map(); // id -> { full, source }

  // Custom (non-vendored, or explicitly-pinned) symbols take priority, e.g.
  // #si-claude pinned from claude-desktop.html — the pass-100,
  // live-app-verified copy — even though simple-icons also vendors an id
  // with the same name.
  if (fs.existsSync(customSymbolsPath)) {
    const text = fs.readFileSync(customSymbolsPath, "utf8");
    const symbols = parseSymbols(text);
    for (const [id, sym] of symbols) {
      canonical.set(id, { full: sym.full, source: customSymbolsPath });
    }
  }

  const sources = loadManifestSpriteFiles();
  for (const spritePath of sources) {
    if (!fs.existsSync(spritePath)) {
      console.warn(`warning: manifest output missing on disk: ${spritePath}`);
      continue;
    }
    const text = fs.readFileSync(spritePath, "utf8");
    const symbols = parseSymbols(text);
    for (const [id, sym] of symbols) {
      if (canonical.has(id)) {
        // Already pinned by backlot-custom-symbols.svg (e.g. si-claude); the
        // pinned custom copy wins over the generic vendored sprite.
        continue;
      }
      canonical.set(id, { full: sym.full, source: spritePath });
    }
  }

  return canonical;
}

// ---------------------------------------------------------------------------
// --write-custom-symbols: regenerate backlot-custom-symbols.svg
// ---------------------------------------------------------------------------

function normalizeInnerForCompare(inner) {
  // Ignore incidental whitespace/indentation differences when detecting
  // real drift between copies of a symbol.
  return inner.replace(/\s+/g, " ").trim();
}

// Ids that must be treated as "custom" (pinned in backlot-custom-symbols.svg)
// even though they happen to share a vendored prefix. #si-claude is
// vendored in simple-icons/brand-symbols.svg too, but the pass-100
// live-app-verified copy lives in compositions/claude-desktop.html and we
// want that exact provenance pinned explicitly rather than silently
// relying on the generic vendored sprite.
const FORCE_CUSTOM_IDS = new Set(["si-claude"]);

function writeCustomSymbols(targetFiles) {
  const CANONICAL_SOURCE_FILE = "compositions/claude-desktop.html";
  const canonicalSourcePath = path.join(root, CANONICAL_SOURCE_FILE);
  if (!fs.existsSync(canonicalSourcePath)) {
    console.error(`cannot find canonical source file ${CANONICAL_SOURCE_FILE}`);
    process.exit(1);
  }

  // Known vendored id prefixes; anything else is "custom".
  const vendoredPrefixes = loadVendoredPrefixes();

  // Collect every symbol id -> occurrences across all target files.
  const occurrences = new Map(); // id -> [{ file, inner, mtimeMs }]
  for (const file of targetFiles) {
    const text = fs.readFileSync(file, "utf8");
    const symbols = parseSymbols(text);
    const mtimeMs = fs.statSync(file).mtimeMs;
    for (const [id, sym] of symbols) {
      const isVendored = vendoredPrefixes.some((p) => id.startsWith(p));
      if (isVendored && !FORCE_CUSTOM_IDS.has(id)) continue;
      if (!occurrences.has(id)) occurrences.set(id, []);
      occurrences.get(id).push({ file, inner: sym.full, mtimeMs });
    }
  }

  // Ids that show up in 2+ files (or are force-included) are "duplicated
  // custom symbols" that need a single source of truth.
  const duplicated = [...occurrences.entries()].filter(
    ([id, occ]) => occ.length > 1 || FORCE_CUSTOM_IDS.has(id),
  );

  const conflicts = [];
  const chosen = new Map(); // id -> full symbol markup

  for (const [id, occ] of duplicated) {
    const distinct = new Set(occ.map((o) => normalizeInnerForCompare(o.inner)));
    const fromCanonicalSource = occ.find((o) => o.file === canonicalSourcePath);

    let winner;
    if (fromCanonicalSource) {
      winner = fromCanonicalSource;
    } else {
      // Fall back to the most-recently-modified copy.
      winner = occ.slice().sort((a, b) => b.mtimeMs - a.mtimeMs)[0];
    }
    chosen.set(id, winner.inner);

    if (distinct.size > 1) {
      conflicts.push({
        id,
        files: occ.map((o) => path.relative(root, o.file)),
        winner: path.relative(root, winner.file),
      });
    }
  }

  const header = [
    "<!-- Canonical custom (non-vendored) icon symbols shared across UI Backlot -->",
    "<!-- compositions/ and surfaces/ HTML. Generated/curated by -->",
    "<!-- tools/sync-inline-icons.mjs --write-custom-symbols. -->",
    `<!-- #si-claude: exact Claude sunburst mark, pinned from ${CANONICAL_SOURCE_FILE} (pass-100, live-app-verified). -->`,
  ].join("\n");

  const symbolBlocks = [...chosen.entries()]
    .sort(([a], [b]) => a.localeCompare(b))
    .map(([, full]) => reindentSymbol(full, "  ", true));

  const sprite = `${header}\n<svg xmlns="http://www.w3.org/2000/svg" style="display:none" aria-hidden="true">\n${symbolBlocks.join("\n")}\n</svg>\n`;

  fs.mkdirSync(path.dirname(customSymbolsPath), { recursive: true });
  fs.writeFileSync(customSymbolsPath, sprite);
  console.log(
    `wrote ${path.relative(root, customSymbolsPath)} (${chosen.size} custom symbols, ${conflicts.length} conflicts)`,
  );

  if (conflicts.length) {
    console.log("\nDrift conflicts resolved (old vs new path data differed):");
    for (const c of conflicts) {
      console.log(`  - #${c.id}: chose ${c.winner}; seen in ${c.files.join(", ")}`);
    }
  }

  return { chosen, conflicts };
}

function loadVendoredPrefixes() {
  const manifest = JSON.parse(fs.readFileSync(manifestPath, "utf8"));
  return manifest.families.map((fam) => `${fam.prefix}-`);
}

// ---------------------------------------------------------------------------
// --check / --write: sync each target file against the canonical map
// ---------------------------------------------------------------------------

function syncFile(file, canonical, write) {
  const text = fs.readFileSync(file, "utf8");
  const symbols = parseSymbols(text);

  const matched = [];
  const replaced = [];
  const unknown = [];
  const skippedProtected = [];

  let out = text;

  for (const [id, sym] of symbols) {
    const canon = canonical.get(id);
    if (!canon) {
      unknown.push(id);
      continue;
    }
    matched.push(id);

    if (sym.protected) {
      // Deliberate hand-authored override (see the comment above the
      // <symbol> tag) — never sync this one from the canonical map.
      skippedProtected.push({ id, reason: sym.protectReason });
      continue;
    }

    const normalizedCurrent = normalizeInnerForCompare(sym.full);
    const normalizedCanonical = normalizeInnerForCompare(canon.full);
    if (normalizedCurrent === normalizedCanonical) continue;

    // Re-indent the canonical symbol to match this file's indentation for
    // the <symbol> line itself, so diffs stay minimal/readable.
    const indentMatch = text.slice(0, text.indexOf(sym.full)).match(/([ \t]*)$/);
    const indent = indentMatch ? indentMatch[1] : "";
    const reindented = reindentSymbol(canon.full, indent);

    out = out.replace(sym.full, () => reindented);
    replaced.push(id);
  }

  const drifted = replaced.length > 0;

  if (write && drifted) {
    fs.writeFileSync(file, out);
  }

  return { file, matched, replaced, unknown, skippedProtected, drifted };
}

function reindentSymbol(full, indent, applyFirstLineIndent = false) {
  const lines = full.split("\n");
  return lines
    .map((line, i) => {
      if (i === 0) return applyFirstLineIndent ? `${indent}${line}` : line;
      if (line.trim() === "") return line;
      return `${indent}${line.replace(/^\s+/, "")}`;
    })
    .join("\n");
}

// ---------------------------------------------------------------------------
// Main
// ---------------------------------------------------------------------------

function defaultTargetFiles() {
  const dirs = ["compositions", "surfaces"];
  const files = [];
  for (const dir of dirs) {
    const dirPath = path.join(root, dir);
    if (!fs.existsSync(dirPath)) continue;
    for (const name of fs.readdirSync(dirPath)) {
      if (name.endsWith(".html")) files.push(path.join(dirPath, name));
    }
  }
  return files.sort();
}

function main() {
  if (mode === "write-custom-symbols") {
    const targets = fileArgs.length
      ? fileArgs.map((f) => path.resolve(root, f))
      : defaultTargetFiles();
    writeCustomSymbols(targets);
    return;
  }

  const canonical = buildCanonicalMap();
  const targets = fileArgs.length
    ? fileArgs.map((f) => path.resolve(root, f))
    : defaultTargetFiles();

  const write = mode === "write";
  let anyDrift = false;
  let totalReplaced = 0;
  const allUnknown = new Set();

  for (const file of targets) {
    if (!fs.existsSync(file)) {
      console.warn(`skipping missing file: ${file}`);
      continue;
    }
    const text = fs.readFileSync(file, "utf8");
    if (!/<symbol\b/.test(text)) continue; // no inline symbols, nothing to do

    const result = syncFile(file, canonical, write);
    const relFile = path.relative(root, result.file);

    if (
      result.matched.length === 0 &&
      result.unknown.length === 0 &&
      result.skippedProtected.length === 0
    ) {
      continue;
    }

    const lines = [`${relFile}:`];
    lines.push(`  matched: ${result.matched.length}`);
    if (result.replaced.length) {
      lines.push(`  replaced (${result.replaced.length}): ${result.replaced.join(", ")}`);
    } else {
      lines.push(`  replaced: 0`);
    }
    if (result.skippedProtected.length) {
      lines.push(
        `  protected (${result.skippedProtected.length}, hand-authored override, left untouched): ` +
          result.skippedProtected.map((s) => `${s.id} [${s.reason}]`).join("; "),
      );
    }
    if (result.unknown.length) {
      lines.push(`  unknown (${result.unknown.length}, left untouched): ${result.unknown.join(", ")}`);
      for (const id of result.unknown) allUnknown.add(id);
    }
    console.log(lines.join("\n"));

    if (result.drifted) {
      anyDrift = true;
      totalReplaced += result.replaced.length;
    }
  }

  console.log("");
  if (mode === "check") {
    if (anyDrift) {
      console.log(`DRIFT DETECTED: ${totalReplaced} symbol(s) across the checked files differ from canonical sources.`);
      if (allUnknown.size) {
        console.log(`(${allUnknown.size} unknown symbol id(s) not in canonical map, not counted as drift.)`);
      }
      process.exit(1);
    } else {
      console.log("No drift: all matched inline symbols match their canonical sources.");
      process.exit(0);
    }
  } else {
    console.log(`Wrote updates: ${totalReplaced} symbol(s) replaced across ${targets.length} file(s) checked.`);
  }
}

main();
