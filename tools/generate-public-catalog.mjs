#!/usr/bin/env node
import fs from "node:fs";
import path from "node:path";

const repoRoot = process.cwd();
const registryPath = path.join(repoRoot, "surfaces", "registry.json");
const outputPath = path.join(repoRoot, "docs", "catalog.md");
const registry = JSON.parse(fs.readFileSync(registryPath, "utf8"));

const groups = [
  {
    title: "Start Here",
    match: (surface) => surface.kind === "workflow",
    note: "Workflow wrappers are the fastest path to a video because they already combine multiple surfaces."
  },
  {
    title: "Claude And Chat",
    match: (surface) => surface.tags?.includes("claude") && surface.kind === "component",
    note: "Claude components can be paired with any app surface when a demo needs an assistant, chat pane, composer, progress rail, or completion state."
  },
  {
    title: "Desktop And Browser",
    match: (surface) => ["macos", "mac", "finder", "browser", "web-app", "calendar"].some((tag) => surface.tags?.includes(tag)),
    note: "Desktop context surfaces establish the environment around a product demo."
  },
  {
    title: "Productivity And Creative Apps",
    match: (surface) => ["powerpoint", "presentation", "word", "excel", "figma", "premiere", "office", "spreadsheet", "document", "video"].some((tag) => surface.tags?.includes(tag)),
    note: "App surfaces are editable HTML/CSS approximations for instructional scenes and workflow videos."
  },
  {
    title: "Codex And Terminal",
    match: (surface) => ["codex", "terminal", "cli", "developer-workflow"].some((tag) => surface.tags?.includes(tag)),
    note: "Developer surfaces support agent coding, terminal, and review workflows."
  }
];

const byId = new Map(registry.surfaces.map((surface) => [surface.id, surface]));
const relLink = (value) => value && fs.existsSync(path.join(repoRoot, value)) ? `[${value}](../${value})` : value || "";
const tags = (surface) => (surface.tags || []).map((tag) => `\`${tag}\``).join(" ");

function row(surface) {
  const capture = surface.capture?.script ? `\`${surface.capture.script}\`` : surface.capture?.status || "";
  return `| \`${surface.id}\` | ${surface.kind} | ${surface.title} | ${relLink(surface.source)} | ${capture} | ${tags(surface)} |`;
}

const recommended = [
  "claude-browser-chat-pane-workflow",
  "claude-presentation-chat-pane-workflow",
  "claude-finder-workflow",
  "claude-code-figma-workflow",
  "claude-codex-terminal-workflow",
  "claude-app",
  "browser-app",
  "presentation-editor",
  "figma-editor",
  "codex-terminal"
].map((id) => byId.get(id)).filter(Boolean);

const lines = [
  "# UI Backlot Catalog",
  "",
  "<!-- Generated by `npm run catalog:generate`. Edit `surfaces/registry.json` instead of this file. -->",
  "",
  "This is the public, agent-readable catalog of editable UI surfaces in UI Backlot. Each row points to the source HTML, import shape, capture command, and tags an agent can use to choose surfaces for a HyperFrames demo.",
  "",
  "## Agent Selection Recipe",
  "",
  "1. Choose one `workflow` when the requested video already matches a pair such as Claude plus browser, Finder, PowerPoint, Word, Excel, Figma, Premiere, or terminal.",
  "2. Choose individual `component` entries when a video needs custom assembly. Mount them with `data-backlot-mount-src` and `data-backlot-mount-selector` using `runtime/backlot-component-loader.js`.",
  "3. Prefer surfaces whose tags match the app, scene, and interaction in the prompt. Use `recommendedUse`, `sourceEvidence`, and `assetDecision` from `surfaces/registry.json` when you need more detail.",
  "4. Run the listed capture script for at least one touched surface, then run the HyperFrames checks before rendering.",
  "",
  "## Recommended Starters",
  "",
  "| id | kind | title | source | capture | tags |",
  "| --- | --- | --- | --- | --- | --- |",
  ...recommended.map(row),
  ""
];

for (const group of groups) {
  const surfaces = registry.surfaces.filter(group.match);
  lines.push(`## ${group.title}`, "", group.note, "", "| id | kind | title | source | capture | tags |", "| --- | --- | --- | --- | --- | --- |", ...surfaces.map(row), "");
}

lines.push(
  "## Machine Source",
  "",
  "The authoritative machine-readable source is [`surfaces/registry.json`](../surfaces/registry.json). Validate it with:",
  "",
  "```bash",
  "npm run registry:check",
  "```",
  ""
);

fs.writeFileSync(outputPath, `${lines.join("\n")}\n`);
console.log(outputPath);
