#!/usr/bin/env node
/**
 * build-wallpaper-surface.mjs
 *
 * Fuses the two catalog halves into the shippable artifacts:
 *   - assets/wallpapers/manifest.json  (real bundled images)
 *   - tools/wallpapers/abstract-set.json (CSS gradients/colors/patterns)
 * into:
 *   - compositions/mac-wallpaper.html   (one parameterized data-wallpaper surface)
 *   - surfaces/wallpapers.json          (unified machine-readable catalog)
 *
 * Regenerate after re-running build-wallpaper-assets.mjs or editing the
 * abstract set. The surface is pure CSS (image wallpapers -> background-image,
 * abstract -> background) so it renders with no JS when mounted in a scene.
 */
import { readFileSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const ROOT = resolve(dirname(fileURLToPath(import.meta.url)), "..", "..");
const images = JSON.parse(readFileSync(resolve(ROOT, "assets/wallpapers/manifest.json"), "utf8")).images;
const abstract = JSON.parse(readFileSync(resolve(ROOT, "tools/wallpapers/abstract-set.json"), "utf8")).wallpapers;

const DEFAULT = "sonoma";

// ---- unified catalog (surfaces/wallpapers.json) ----------------------------
const catalog = [
  ...images.map((i) => ({
    id: i.id,
    name: i.name,
    version: i.version,
    category: i.category, // lineage | color
    type: "image",
    asset: i.asset,
    thumb: i.thumb,
    provenance: i.provenance,
  })),
  ...abstract.map((a) => ({
    id: a.id,
    name: a.name,
    category: a.category, // metallic | gradient | pattern | solid
    type: "css",
    css: a.css,
  })),
];

writeFileSync(
  resolve(ROOT, "surfaces/wallpapers.json"),
  JSON.stringify(
    {
      generated: "tools/wallpapers/build-wallpaper-surface.mjs",
      surface: "mac-wallpaper",
      default: DEFAULT,
      count: catalog.length,
      wallpapers: catalog,
    },
    null,
    2,
  ) + "\n",
);

// ---- CSS rules -------------------------------------------------------------
// Scope every wallpaper rule with the #id so it outranks the base rule's
// fallback background-color (ID+attr 1,1,0 > ID 1,0,0). Without this, solid
// colours paint only via background-color and lose to the fallback.
const S = "#mac-wallpaper-surface";
const imageRules = images
  .map(
    (i) =>
      `          ${S}[data-wallpaper="${i.id}"] { background-image: url("../${i.asset}"); }`,
  )
  .join("\n");

const abstractRules = abstract
  .map((a) => `          ${S}[data-wallpaper="${a.id}"] { background: ${a.css}; }`)
  .join("\n");

const html = `<!DOCTYPE html>
<html lang="en">
  <head>
    <meta charset="UTF-8" />
    <title>macOS Wallpaper Catalog</title>
    <!--
      GENERATED by tools/wallpapers/build-wallpaper-surface.mjs — do not edit by
      hand. Add images via tools/wallpapers/build-wallpaper-assets.mjs and CSS
      wallpapers via tools/wallpapers/abstract-set.json, then re-run the surface
      generator (npm run wallpapers:build).

      One parameterized wallpaper surface. Pick a wallpaper with
      data-wallpaper="ID" on the root (or ?wallpaper=ID when previewed
      standalone). Photographic wallpapers are real bundled JPEGs under
      assets/wallpapers/macos/; gradients, metallics, solid colours and patterns
      are pure CSS. Both paths are attribute-driven CSS, so the surface renders
      with no JS when the backlot loader mounts it into a scene.
    -->
  </head>
  <body>
    <template id="mac-wallpaper-surface-template">
      <div id="mac-wallpaper-surface" data-composition-id="mac-wallpaper-surface" data-width="1920" data-height="1080" data-wallpaper="${DEFAULT}" data-primitive="mac-wallpaper" aria-label="macOS desktop wallpaper">
        <style>
          #mac-wallpaper-surface {
            position: relative;
            width: 1920px;
            height: 1080px;
            overflow: hidden;
            background-color: #0b1020;
            background-size: cover;
            background-position: center;
            background-repeat: no-repeat;
          }

          /* ==== Photographic wallpapers — real bundled images ============== */
${imageRules}

          /* ==== CSS wallpapers — gradients, metallics, colours, patterns === */
${abstractRules}
        </style>
      </div>
    </template>

    <script>
      /* Direct-preview bootstrap: clone the template into the body when this
         file is loaded on its own (capture/preview), and honor ?wallpaper=ID.
         Stripped by the backlot loader when mounted — the wallpaper is chosen
         purely by the data-wallpaper attribute + CSS, so no runtime is needed. */
      (() => {
        if (!/\\/mac-wallpaper\\.html$/.test(window.location.pathname)) return;
        const template = document.getElementById("mac-wallpaper-surface-template");
        const mount = () => {
          if (!template || document.querySelector("#mac-wallpaper-surface")) return;
          const fragment = template.content.cloneNode(true);
          fragment.querySelectorAll("script").forEach((s) => s.remove());
          document.body.appendChild(fragment);
          const id = new URLSearchParams(window.location.search).get("wallpaper");
          if (id) document.getElementById("mac-wallpaper-surface").setAttribute("data-wallpaper", id);
        };
        if (document.readyState === "loading") document.addEventListener("DOMContentLoaded", mount);
        else mount();
      })();
    </script>
  </body>
</html>
`;

writeFileSync(resolve(ROOT, "compositions/mac-wallpaper.html"), html);
console.log(
  `Generated compositions/mac-wallpaper.html + surfaces/wallpapers.json — ` +
    `${catalog.length} wallpapers (${images.length} image, ${abstract.length} css)`,
);
